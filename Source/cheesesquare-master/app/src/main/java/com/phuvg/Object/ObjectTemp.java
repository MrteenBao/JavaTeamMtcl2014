public class ObjectTemp {
    private String s1;
    private String s2;
    private int n1;

    public ObjectTemp(){

    }

    public ObjectTemp(String s1, String s2, int n1){
        this.s1 = s1;
        this.s2 = s2;
        this.n1 = n1;
    }

    public String getS1() {
        return s1;
    }

    public void setS1(String s1) {
        this.s1 = s1;
    }

    public String getS2() {
        return s2;
    }

    public void setS2(String s2) {
        this.s2 = s2;
    }

    public int getN1() {
        return n1;
    }

    public void setN1(int n1) {
        this.n1 = n1;
    }
}
